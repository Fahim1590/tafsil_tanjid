    public static function schema(string $type, ?array $entry = null): array
    {
        $site = self::site();
        $localBusiness = [
            '@context' => 'https://schema.org',
            '@type' => ['LocalBusiness', 'HomeAndConstructionBusiness'],
            '@id' => $site['baseUrl'] . '/#business',
            'name' => $site['brand'],
            'url' => $site['baseUrl'],
            'image' => $site['baseUrl'] . self::image('luxury-curtains-living-room-optimized.jpg'),
            'telephone' => $site['phoneTel'],
            'email' => $site['email'],
            'priceRange' => '$$',
            'address' => [
                '@type' => 'PostalAddress',
                'addressLocality' => 'Riyadh',
                'addressCountry' => 'SA',
            ],
            'areaServed' => [
                ['@type' => 'City', 'name' => 'Riyadh'],
            ],
            'openingHoursSpecification' => [
                [
                    '@type' => 'OpeningHoursSpecification',
                    'dayOfWeek' => ['Saturday', 'Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday'],
                    'opens' => '09:00',
                    'closes' => '22:00',
                ],
            ],
            'sameAs' => [],
            'hasOfferCatalog' => [
                '@type' => 'OfferCatalog',
                'name' => 'خدمات الستائر والتنجيد في الرياض',
                'itemListElement' => array_map(static function (array $service): array {
                    return [
                        '@type' => 'Offer',
                        'itemOffered' => [
                            '@type' => 'Service',
                            'name' => $service['h1'],
                            'areaServed' => 'Riyadh',
                        ],
                    ];
                }, array_values(self::services())),
            ],
        ];

        $schemas = [$localBusiness];

        if ($type === 'service' && $entry !== null) {
            $schemas[] = [
                '@context' => 'https://schema.org',
                '@type' => 'Service',
                'name' => $entry['h1'],
                'description' => $entry['summary'],
                'provider' => ['@id' => $site['baseUrl'] . '/#business'],
                'areaServed' => ['@type' => 'City', 'name' => 'Riyadh'],
                'serviceType' => implode(', ', $entry['keywords']),
            ];
            $schemas[] = self::faqSchema($entry['faq']);
        }

        if ($type === 'home') {
            $schemas[] = self::faqSchema(self::homeFaqs());
        }

        if ($type === 'article' && $entry !== null) {
            $schemas[] = [
                '@context' => 'https://schema.org',
                '@type' => 'Article',
                'headline' => $entry['title'],
                'description' => $entry['summary'],
                'image' => $site['baseUrl'] . $entry['image'],
                'author' => ['@type' => 'Organization', 'name' => $site['brand']],
                'publisher' => ['@type' => 'Organization', 'name' => $site['brand']],
                'mainEntityOfPage' => self::canonical('blog/' . $entry['slug']),
            ];
        }

        return $schemas;
    }

    private static function faqSchema(array $faqs): array
    {
        return [
            '@context' => 'https://schema.org',
            '@type' => 'FAQPage',
            'mainEntity' => array_map(static function (array $faq): array {
                return [
                    '@type' => 'Question',
                    'name' => $faq['q'],
                    'acceptedAnswer' => [
                        '@type' => 'Answer',
                        'text' => $faq['a'],
                    ],
                ];
            }, $faqs),
        ];
    }
